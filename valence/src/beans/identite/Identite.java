	/** calcul de l'age par rapport a la date indiquee en parametre
	 * @param j une date
	 * @return l'age de la personne si le parametre et la date de naissance de la personne ne sont pas nulls
	 *  */
	public int calculerAge(Date j) {
		Calendar jour = Calendar.getInstance();
		Calendar naissance = Calendar.getInstance();

		if (j != null && this.getDatenais_IDE() != null) {
			jour.setTime(j);
			naissance.setTime(this.getDatenais_IDE());
			int age = jour.get(Calendar.YEAR) - naissance.get(Calendar.YEAR);
			jour.add(Calendar.YEAR, -age);
			if (naissance.after(jour))
				age = age - 1;
			return age;
		} else
			return -1;

	}

	/**
	 * permet de calculer la duree d'inscription a Pole emploi par rapport a la
	 * date d'accueil
	 * @return le nombre d'annees depuis la date d'accueil ou -1 si celle ci n'est pas enregistrée
	 */
	public int calculerTempsPE() {
		Calendar jour = Calendar.getInstance();
		Calendar inscrip = Calendar.getInstance();
		if (this.getDateAccueil_IDE() != null
				&& this.getPoleEmploiInscripription_IDE() != null) {
			inscrip.setTime(this.getPoleEmploiInscripription_IDE());
			jour.setTime(this.getDateAccueil_IDE());
			int an = jour.get(Calendar.YEAR) - inscrip.get(Calendar.YEAR);
			jour.add(Calendar.YEAR, -an);
			if (inscrip.after(jour))
				an = an - 1;
			return an;

		} else
			return -1;

	}

	/** permet de calculer la duree d'inscription a Pole emploi par rapport a une autre date
	 * 
	 * @param d une date quelconque
	 * @return le nombre d'annees depuis la date d'inscription a POLE EMPLOI  ou -1 si celle ci n'est pas enregistrée
	 */
	
	public int calculerTempsPE(Date d) {
		Calendar jour = Calendar.getInstance();
		Calendar inscrip = Calendar.getInstance();
		if (this.getPoleEmploiInscripription_IDE() != null) {
			inscrip.setTime(this.getPoleEmploiInscripription_IDE());
			jour.setTime(d);
			//int an = jour.get(Calendar.YEAR) - inscrip.get(Calendar.YEAR);
			//jour.add(Calendar.YEAR, -an);
			long diff;
			if (inscrip.before(jour)){
				diff=jour.getTimeInMillis()-inscrip.getTimeInMillis();
				diff=diff/(86400000);
				double resultat=diff/365;
				return (int) resultat;
			}
			else
			return -1;

		} else
			return -1;

	}

